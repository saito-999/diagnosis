function emptySection() {
  return { bullets: [], sentences: [] };
}

function emptyPayload() {
  return {
    sections: {
      scene: emptySection(),
      why: emptySection(),
      awareness: emptySection(),
      recommend: emptySection(),
    }
  };
}

function normalizePayload(x) {
  const base = emptyPayload();
  if (!x || typeof x !== "object") return base;

  const s = (x.sections && typeof x.sections === "object") ? x.sections : null;
  if (!s) return base;

  for (const k of ["scene","why","awareness","recommend"]) {
    const sec = (s[k] && typeof s[k] === "object") ? s[k] : null;
    if (!sec) continue;

    if (Array.isArray(sec.bullets)) base.sections[k].bullets = sec.bullets.map(String);
    if (Array.isArray(sec.sentences)) base.sections[k].sentences = sec.sentences.map(String);
  }
  return base;
}

export function getText(phaseKey, patternKey) {
  const phase = (TEXTS && TEXTS[phaseKey]) ? TEXTS[phaseKey] : null;
  if (!phase) return emptyPayload();

  const picked = (patternKey && phase[patternKey]) ? phase[patternKey] : null;
  const fallback = phase._default || null;

  return normalizePayload(picked || fallback);
}
